<div class="content-content">
    <h2 class="sectionHeader">Erlkönig</h2>
    <div class="section section-erlkoenig">
        <p>Wer reitet so spät durch Nacht und Wind?</p>       
        <p>Es ist der Vater mit seinem Kind.</p>
        <p>Er hat den Knaben wohl in dem Arm,</p>
        <p>Er fasst ihn sicher, er hält ihn warm.</p> 
    </div>
    <div class="section section-erlkoenig">
        <p>Mein Sohn, was birgst du so bang dein Gesicht? –</p>
        <p>Siehst, Vater, du den Erlkönig nicht?</p>
        <p>Den Erlkönig mit Kron’ und Schweif? –</p>
        <p>Mein Sohn, es ist ein Nebelstreif. –</p>
    </div>
    <div class="section section-erlkoenig">
        <p>„Du liebes Kind, komm, geh mit mir!</p>
        <p>„Du liebes Kind, komm, geh mit mir!</p>
        <p>Gar schöne Spiele spiel’ ich mit dir;</p>
        <p>Manch’ bunte Blumen sind an dem Strand,</p>
        <p>Meine Mutter hat manch gülden Gewand.“ –</p>
    </div>
    <div class="section section-erlkoenig">
        <p>Mein Vater, mein Vater, und hörest du nicht,</p>
        <p>Was Erlkönig mir leise verspricht? –</p>
        <p>Sei ruhig, bleibe ruhig, mein Kind;</p>
        <p>In dürren Blättern säuselt der Wind. –</p>
    </div>
    <div class="section section-erlkoenig">
        <p>"Willst, feiner Knabe, du mit mir gehn?</p>
        <p>Meine Töchter sollen dich warten schön;</p>
        <p>Meine Töchter führen den nächtlichen Reihn</p>
        <p>Und wiegen und tanzen und singen dich ein.“ –</p>
    </div>
    <div class="section section-erlkoenig">
        <p>Mein Vater, mein Vater, und siehst du nicht dort</p>
        <p>Erlkönigs Töchter am düstern Ort? –</p>
        <p>Mein Sohn, mein Sohn, ich seh’ es genau:</p>
        <p>Es scheinen die alten Weiden so grau. –</p>
    </div>
    <div class="section section-erlkoenig">
        <p>„Ich liebe dich, mich reizt deine schöne Gestalt;</p>
        <p>Und bist du nicht willig, so brauch’ ich Gewalt.“ –</p>
        <p>Mein Vater, mein Vater, jetzt faßt er mich an!</p>
        <p>Erlkönig hat mir ein Leids getan! –</p>
    </div>
    <div class="section section-erlkoenig">
        <p>Dem Vater grauset’s; er reitet geschwind,</p>
        <p>Er hält in Armen das ächzende Kind,</p>
        <p>Erreicht den Hof mit Mühe und Not;</p>
        <p>In seinen Armen das Kind war tot.</p>
    </div>
</div>
