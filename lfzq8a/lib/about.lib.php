<h2 class="sectionHeader">Unsere Chefköchin – Sarangerel Batbayar</h2>
<div class="section chef-flex">
  <img class="chef-img" src="images/ueber-uns/bolormaa.png" alt="Chefköchin Sarangerel Batbayar" loading="lazy">
  <div class="chef-text">
    <p><em>Sarangerel</em> („Mondlicht“ auf Mongolisch) wuchs in einer Jurte in der Weite der Zentralmongolei auf – mit Teigtaschen vom
    offenen Feuer und frisch gegrilltem Fisch über glühenden Kohlen.</p><br>
    <p>Ihre kulinarische Reise führte sie von den Steppenmärkten in Ulaanbaatar zu Sushi-Meistern in Kyoto und schließlich nach Europa. Heute vereint sie diese Welten in maßgeschneiderten Menüs, Live-Cooking-Formaten und feinen Details, die Geschichten erzählen.</p>
  </div>
</div>

