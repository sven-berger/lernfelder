public class MathematischeOperatoren {

    public static void main(String[] args) {
        int w = 5;
        int x = 1;
        int y = 2;
        int z = 10;

        // Addieren
        System.out.print(y + z + "\n \n");

        // Subtrahieren
        System.out.print(z - w + "\n \n");

        // Multiplikation
        System.out.print(x * z + "\n \n");

        // Division
        System.out.print(z / y + "\n \n");

        // Modulo-Rechnung
        System.out.print(z % 5 + "\n \n");

        // Punkt vor Strich - Die Formeln in Klammern ist freiwillig
        System.out.print(x + (z * y) + "\n \n");
    }

}